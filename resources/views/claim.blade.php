             <h3>Claims – Motor Insurance</h3>
             <div class="inner-box">
             <h4><b>Step 1</b>: Register your claim</h4>
             <p><b>Option A</b>: To register your claim online, please click -  <a href="https://general.bajajallianz.com/BagicNxt/MotClaims/index.jsp">https://general.bajajallianz.com/BagicNxt/MotClaims/index.jsp</a></p>
             <p><b>Option B</b>: To register your claim on the phone, please dial Toll Free Number: 1800-209-5858</p>

             <ul class="list">
             	<li>You will need to provide:</li>
             	<li>Contact number</li>
             	<li>Engine and Chassis Number</li>
             	<li>Accident date and time</li>
             	<li>Description and location of the accident</li>
             	<li>Vehicle inspection address</li>
             	<li>Km. reading</li>
             	<li>Once the claim is registered, the customer support executive will provide you with a Claim Reference Number.</li>
             </ul>

             <h4><b>Step 2</b>: Send your Vehicle for Repair</h4>
             <ul class="list">
             	<li><b>In case of an accident:</b> Take your vehicle to the garage (if movable) or have it towed to avoid further damage.</li>
             	<li><b>In case of a theft claim:</b> File a police complaint immediately in writing and inform us on our Toll Free Number. If your vehicle isn't found within 90 days, ask the police to issue you a Non-Traceable Report. This is an undertaking given by the police that they are yet to find your vehicle; you need to submit this report to us. We start the claim process after this.</li>
             </ul>

             <h4><b>Step 3:</b> Survey and Claim Settlement</h4>

             <ul class="list">
             	<li>Documents Required: Submit the copy of documents to the garage / dealer and get it verified with the originals.</li>
             	<li>For the list of documents required to process the claim, please refer to the checklist above.</li>
             	<li>Spot Settlement: If the loss is not severe, we suggest that you avail spot settlement of claims through Bajaj Allianz Mobile Settlement Service.</li>
             	<li>Claim Settlement:</li>
             	<li>After completion of repair at a preferred garage we will make payment directly to the garage.</li>
             	<li>You will only have to pay the excess as mentioned in the policy and the depreciation value, salvage etc. as informed by the surveyor.</li>

